import json
import random
from torch.utils.data import Dataset
import cv2

class StanfordCarsDataset(Dataset):
    """ A PyTorch Dataset class for the Stanford Cars dataset.
    Args:
        data_records: List of dicts, e.g., [{"image_path": "car1.jpg", "label_name": "Audi A5 Coupe 2012", "bbox": {"x1": 10, "y1": 20, "x2": 200, "y2": 150}}]
        processor: The HuggingFace processor
        llm_captions_path: Path to the JSON file generated by the offline LLM script
        split: "train" or "test"
    """

    def __init__(self, data_records: list, processor: object, llm_captions_path: str = "llm_captions.json", split: str = "train"):
        self.data_records = data_records
        self.processor = processor
        self.split = split
        
        # Load the pre-computed LLM captions directly into memory
        with open(llm_captions_path, "r") as f:
            self.llm_captions = json.load(f)

    def __len__(self):
        """Returns the total number of samples in the dataset."""

        return len(self.data_records)

    def __getitem__(self, idx: int) -> dict:
        """Fetches the image and corresponding caption for a given index, applies necessary transformations, and returns them as tensors.
        Args:
            idx: The index of the sample to fetch.
        Returns:
            A dictionary containing:
                - "pixel_values": The processed image tensor ready for model input. Shape: (3, 224, 224)
                - "input_ids": The tokenized caption tensor ready for model input. Shape: (max_length,)
                - "attention_mask": The attention mask tensor for the caption. Shape: (max_length,). 
                    Optional, only if the processor returns it.
                - "raw_text": The original caption string (useful for debugging or analysis)
        """

        record = self.data_records[idx]
        image_path = record["image_path"]
        label_name = record["label_name"]
        bbox = record["bbox"]

        # Load the image
        try:
            image_bgr = cv2.imread(image_path)

            if image_bgr is None:
                raise ValueError(f"Corrupted or missing file: {image_path}")
            
            if bbox is not None:
                image_bgr = image_bgr[bbox["y1"]:bbox["y2"], bbox["x1"]:bbox["x2"]]

            image = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
        except Exception as e:
            print(f"Data Warning: {e}. Sampling fallback image.")

            # Returning a blak image is not a good idea.
            # Instead, recursively sample another random valid image from the dataset.
            fallback_idx = random.randint(0, len(self.data_records) - 1)
            return self.__getitem__(fallback_idx)
        
        # Fetch the list of augmented captions for this specific class
        available_captions = self.llm_captions.get(label_name, [f"A photo of a {label_name}."])
        
        # Select the caption based on the split
        if self.split == "train":
            # During training, randomly select one of the LLM captions to prevent syntactic overfitting
            caption = random.choice(available_captions)
        else:
            # During testing/validation, strictly use the templated label
            # to ensure stable, isolated evaluation of the visual features.
            caption = f"A photo of a {label_name}."
        
        # Process inputs for the model
        inputs = self.processor(
            text=caption, 
            images=image, 
            return_tensors="pt", 
            padding="max_length",
            truncation=True
        )

        result = {
            "pixel_values": inputs.pixel_values.squeeze(0),
            "input_ids": inputs.input_ids.squeeze(0),
            "class_id": record["class_id"],
            "image_path": image_path,
            "label_name": label_name,
            "raw_text": caption 
        }

        # The attention mask is only needed if the processor actually returns it
        if "attention_mask" in inputs:
            result["attention_mask"] = inputs.attention_mask.squeeze(0)
            
        return result